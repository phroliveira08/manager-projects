    private void confirmarjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmarjButtonActionPerformed
        // TODO add your handling code here:   
        int idProjeto = _projeto.getId_projeto();
        Pessoa pessoa = _pessoaBusiness.getPessoaLogada();
        String username = pessoa.getUsername();
        
        Object prioridadeItem = prioridadejComboBox.getSelectedItem();
        Object complexidadeItem = complexidadejComboBox.getSelectedItem();
        Object estadoItem = estadojComboBox.getSelectedItem();
        String esforcoEmHorasText = esforcoEmHorasjTextField.getText();
        
        Requisito requisito = new Requisito();
        
        String nome = nomejTextField.getText();
        String modulo = modulojTextField.getText();
        String funcionalidade = funcionalidadejTextField.getText();
        float versao = 0;
        String prioridade = (String)prioridadeItem;
        String complexidade = (String)complexidadeItem;
        int esforcoEmHoras = Integer.valueOf(esforcoEmHorasText);
        String estado = (String)estadoItem;
        String fase = fasejTextField.getText();
        String descricao = descricaojTextArea.getText();
        Date dataUltimaAlteracao = new Date();
        String autorUltimaModificacao = username;
        
        requisito.setNome(nome);
        requisito.setModulo(modulo);
        requisito.setFuncionalidade(funcionalidade);
        requisito.setVersao(versao);
        requisito.setPrioridade(prioridade);
        requisito.setComplexidade(complexidade);
        requisito.setEsforcoEmHoras(esforcoEmHoras);
        requisito.setEstado(estado);
        requisito.setFase(fase);
        requisito.setDescricao(descricao);
        requisito.setDataUltimaAlteracao(dataUltimaAlteracao);
        requisito.setAutorUltimaModificacao(autorUltimaModificacao);
        requisito.setIdProjeto(idProjeto);
        
       
        if(_finalidadeTela.equals("Atualizar")){
            System.out.println(idProjeto);
            int idRequisito = _requisito.getId();
            Date dataCriacao = _requisito.getDataCriacao();
            String autor = _requisito.getAutor();
            requisito.setAutor(autor);
            requisito.setDataCriacao(dataCriacao);
            requisito.setId(idRequisito);
            this._requisitoBusiness.Atualizar(requisito);
        }else{
            Date dataCriacao = new Date();
            String autor = username;
            requisito.setAutor(autor);
            requisito.setDataCriacao(dataCriacao);
            this._requisitoBusiness.Adicionar(requisito);
        }
        
        this.setVisible(false);
        this.dispose();
    }//GEN-LAST:event_confirmarjButtonActionPerformed

