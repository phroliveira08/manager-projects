    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        // TODO add your handling code here:
        if(_finalidadeTela.equals("Atualizar")){
            confirmarjButton.setText("Atualizar");
            this.setTitle("Atualizar Requisitos");
            String nome = _requisito.getNome();
            String modulo = _requisito.getModulo();
            String funcionalidade = _requisito.getFuncionalidade();
            String prioridade = _requisito.getPrioridade();
            String complexidade = _requisito.getComplexidade();
            int esforcoEmHorasInt = _requisito.getEsforcoEmHoras();
            String estado = _requisito.getEstado();
            String fase = _requisito.getFase();
            String descricao = _requisito.getDescricao();
            
            //Map<String, String> mapPrioridade = new HashMap<>();
            //mapPrioridade.put("","");
            
            String esforcoEmHoras = String.valueOf(esforcoEmHorasInt);
            int prioridadeItem = 0;
            int complexidadeItem = 0;
            int estadoItem = 0;
            
            nomejTextField.setText(nome);
            modulojTextField.setText(modulo);
            funcionalidadejTextField.setText(funcionalidade);
            prioridadejComboBox.setSelectedItem(prioridade);
            complexidadejComboBox.setSelectedIndex(complexidadeItem);
            esforcoEmHorasjTextField.setText(esforcoEmHoras);
            estadojComboBox.setSelectedIndex(estadoItem);
            fasejTextField.setText(fase);
            descricaojTextArea.setText(descricao);
        } else{
            this.setTitle("Cadastrar Requisitos");
        }
    }//GEN-LAST:event_formWindowActivated

